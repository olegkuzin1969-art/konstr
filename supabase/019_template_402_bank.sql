2. ПРАВОВОЕ ОБОСНОВАНИЕ ТРЕБОВАНИЯ

Согласно ч. 1 ст. 9 Федерального закона № 402‑ФЗ, каждый факт хозяйственной жизни подлежит оформлению первичным учетным документом. Первичный учетный документ составляется при совершении факта хозяйственной жизни.

В соответствии с ч. 2 ст. 9 402‑ФЗ, обязательными реквизитами первичного учетного документа являются:
— Наименование документа;
— Дата составления;
— Наименование экономического субъекта, составившего документ;
— Содержание факта хозяйственной жизни;
— Величина натурального и (или) денежного измерения;
— Подписи лиц, совершивших сделку, с указанием их фамилий и инициалов.

Согласно ч. 3 ст. 7 402‑ФЗ, руководитель экономического субъекта (банка) обязан возложить ведение бухгалтерского учета на главного бухгалтера или иное должностное лицо. Главный бухгалтер несет ответственность за достоверность отражения фактов хозяйственной жизни.

Банк, как кредитная организация, обязан вести бухгалтерский учет в соответствии с Положением Банка России № 809‑П «О Плане счетов бухгалтерского учета для кредитных организаций», где счета по учету кредитов (455, 457 и др.) и обязательств перед клиентами (423, 40817 и др.) являются балансовыми счетами.

